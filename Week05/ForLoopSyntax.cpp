#include <iostream>
using namespace std;

int main() {

    // For loop using Increment

    for (int n = 1; n < 10; n++) {
        cout << n << ", ";
    }

    cout << "Reach Out!!!" << endl;

    // For loop using Decrement

    for (int n = 10; n >= 0; n--) {
        cout << n << ", ";
    }

    cout << "Lift Off!!!" << endl;



    return 0;
}