#include <iostream>
using namespace std;

int main() {

    int x = 2;
    int y = 5;
    int z = -25;

    cout << "Power : " << pow(x, y) << endl;

    cout << "Sqrt : " << sqrt(25) << endl;

    cout << "Absolute Value : " << abs(z) << endl;

    cout << "Rounds Up: " << ceil(4.2) << endl;

    cout << "Rounds Down: " << floor(4.8) << endl;

    cout << "Round: " << round(4.2) << endl;

    cout << "Round: " << round(4.8) << endl;

    cout << "Max: " << max(5, 10) << endl;

    cout << "Min: " << min(5, 10) << endl;

    cout << "Log: " << log(2) << endl;
}