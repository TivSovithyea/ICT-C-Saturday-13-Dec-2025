#include <iostream>
using namespace std;

int main() {
    int x = 5, y = 7;
    float a = 3.7, b = 8.0;

    cout << x << " + " << y << " = " << x + y << endl;
    cout << a << " + " << b << " = " << a + b << endl;
    cout << x << " + " << b << " = " << x + b << endl ;
    cout << x  << " - " << y << " = " << x - y<< endl ;
    cout << a << " - " << b << " = " << a - b << endl ;
    cout << x << " - " << b << " = " << x - b << endl ;
    cout << x << " x " << y << " = " << x * y << endl ;
    cout << a << " * " << b << " = " << a * b << endl ;
}
